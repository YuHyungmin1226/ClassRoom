def _term_to_def(bank, term):
    correct = bank[term]
    pool = [d for d in set(bank.values()) if d != correct]
    random.shuffle(pool)
    opts = [correct] + pool[:3]
    random.shuffle(opts)
    return _mk("교과 용어", 2, f"'{term}'에 대한 설명으로 알맞은 것은?",
               opts.index(correct) + 1, q_type='choice', options=' | '.join(opts),
               explanation=f"{term}: {correct}")


def _def_to_term(bank, term):
    pool = [t for t in bank if t != term]
    random.shuffle(pool)
    opts = [term] + pool[:3]
    random.shuffle(opts)
    return _mk("교과 용어", 2, f'다음 설명에 해당하는 것은?  "{bank[term]}"',
               opts.index(term) + 1, q_type='choice', options=' | '.join(opts),
               explanation=f"{term}: {bank[term]}")


def generate_knowledge(subject, grade, n):
    bank = TERMS.get((subject, int(grade)))
    if not bank or len(bank) < 4:
        return []
    gens = []
    for term in bank:
        gens.append(lambda t=term: _term_to_def(bank, t))
        gens.append(lambda t=term: _def_to_term(bank, t))
    return _collect(gens, n)
